	<p>Graham’s Law</p>
	<ul>
	<li>Scottish chemist Thomas Graham (1805–1869) found experimentally that the rate of effusion of a gas is inversely proportional to the square root of the mass of its particles. In other words, the ratio of the rates of effusion of two gases at the same temperature and pressure is given by the inverse ratio of the square roots of the masses of the gas particles.</li>
